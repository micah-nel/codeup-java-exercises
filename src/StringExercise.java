
import java.util.Scanner;
public class StringExercise {

    public static void main(String[] args) {
        String output1 = "We don't need no education";
        System.out.println(output1);

        String output2 = "We don't need no thought control";
        System.out.println(output2);

        String output3 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(output3);

        String output4 = "In windows, the main drive is usually C:\\";
        System.out.println(output4);

        String output5 = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(output5);

    }
}
